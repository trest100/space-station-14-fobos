using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.VotingNew.UI;

[GenerateTypedNameReferences]
public sealed partial class PresetControl : GridContainer
{
    public readonly Dictionary<string, Button> ButtonsList = new();
    public PresetControl()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);
    }

    public void Populate(List<string> presets)
    {
        foreach (var preset in presets)
        {
            var newButton = new Button
            {
                Text = preset,
                ToggleMode = true,
                Access = AccessLevel.Public,
            };

            AddChild(newButton);
            ButtonsList.Add(preset, newButton);
        }
    }
}
